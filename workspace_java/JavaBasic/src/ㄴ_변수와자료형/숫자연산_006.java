package ㄴ_변수와자료형;

public class 숫자연산_006 {
    public static void main(String[] args) {

        System.out.println(10+10);

        //정수끼리의 연산 결과는 정수
        System.out.println(10 / 4);
        //실수끼리의 연산 결괴는 실수
        System.out.println(10.0 / 4.0);
        //정수와 실수의 연산 결과는 실수
        System.out.println(10 / 4.0);
        System.out.println(10 / 3 * 1.5); //4.5
    }
}
